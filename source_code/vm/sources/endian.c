void	convert_endian(char *mem, int len)
{
	int i;
	int tmp;

	i = 0;
	while (i < len / 2)
	{
		tmp = mem[i];
		mem[i] = mem[len - i - 1];
		mem[len - i - 1] = tmp;
		i++;
	}
}
